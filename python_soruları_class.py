"""---
### Soru 10 — Mixin
Aşağıdaki iki Mixin'i yaz ve `Calisan` class'ına ekle:
- `KarsilastirmaMixin`: `__eq__` ve `__lt__` eklesin — karşılaştırma `maas` değerine göre yapılsın
- `YazdirmaMixin`: `yazdir()` metodu tüm instance değişkenlerini `anahtar: değer` formatında yazdırsın
- `Calisan`: `isim`, `departman`, `maas` alsın
"""

# Cevabını buraya yaz
class KarsilastirmaMixin:
    def __eq__(self, diger):
        return self.maas == diger.maas
    def __lt__(self, diger):
        return self.maas < diger.maas
    def __gt__(self, diger):
        return self.maas > diger.maas

class YazdirMixin:
    def yazdir(self):
        for anahtar, deger in self.__dict__.items():
            print(f"{anahtar}: {deger}")

class Calisan(KarsilastirmaMixin, YazdirMixin):
    def __init__(self, isim, departman, maas):
        self.isim = isim
        self.departman = departman
        self.maas = maas

# TEST
c1 = Calisan("Ali", "Yazılım", 50000)
c2 = Calisan("Ayşe", "Tasarım", 60000)
c3 = Calisan("Ali", "Yazılım", 50000)
assert c1 < c2
assert c2 > c1
assert c1 == c3
c1.yazdir()
# isim: Ali
# departman: Yazılım
# maas: 50000
print("✅ Soru 10 doğru!")

"""---
### Soru 11 — Hepsini Birleştir (Proje)
Bir `Kutuphane` sistemi yaz:

**`Kitap` class'ı:**
- `isbn`, `ad`, `yazar`, `mevcut` (varsayılan `True`) alsın
- `__str__` → `"[ISBN] Ad — Yazar"`
- `__repr__` → `"Kitap(isbn, ad, yazar)"`

**`Kutuphane` class'ı:**
- `kitaplar` listesi tut
- `kitap_ekle(kitap)` → listeye ekle
- `__len__` → toplam kitap sayısı
- `__contains__` → ISBN ile kitap var mı kontrolü (`"978..." in kutuphane`)
- `kitap_bul(isbn)` → ISBN ile kitap döndür, bulamazsa `None`
- `odunc_al(isbn)` → kitabı `mevcut=False` yap, zaten alınmışsa hata ver
- `iade_et(isbn)` → kitabı `mevcut=True` yap
- `mevcut_kitaplar()` → sadece mevcut kitapları listele
"""

# Cevabını buraya yaz
class Kitap:
    def __init__(self, isbn, ad, yazar, mevcut=True):
      self.isbn = isbn
      self.ad = ad
      self.yazar = yazar
      self.mevcut = mevcut

    def __str__(self):
        return f"[{self.isbn}] {self.ad} - {self.yazar}"

    def __repr__(self):
        return f"Kitap('{self.isbn}', '{self.ad}', '{self.yazar}')"

class Kutuphane:
    def __init__(self):
        self.kitaplar = []

    def kitap_ekle(self, kitap):
        self.kitaplar.append(kitap)

    def __len__(self):
        return len(self.kitaplar)

    def __contains__(self, isbn):
        return any(k.isbn == isbn for k in self.kitaplar)

    def kitap_bul(self, isbn):
        for k in self.kitaplar:
            if k.isbn == isbn: return k
        return None

    def odunc_al(self, isbn):
        kitap = self.kitap_bul(isbn)
        if kitap and kitap.mevcut:
            kitap.mevcut = False
        else:
            raise Exception("Kitap mevcut değil veya zaten ödünç alınmış.")

    def iade_et(self, isbn):
        kitap = self.kitap_bul(isbn)
        if kitap:
            kitap.mevcut = True

    def mevcut_kitaplar(self):
        return [k for k in self.kitaplar if k.mevcut]

# TEST
k1 = Kitap("978-1", "Suç ve Ceza", "Dostoyevski")
k2 = Kitap("978-2", "1984", "Orwell")
k3 = Kitap("978-3", "Dune", "Herbert")

kutuphane = Kutuphane()
kutuphane.kitap_ekle(k1)
kutuphane.kitap_ekle(k2)
kutuphane.kitap_ekle(k3)

assert len(kutuphane) == 3
assert "978-1" in kutuphane
assert "978-9" not in kutuphane

kutuphane.odunc_al("978-1")
assert k1.mevcut == False
assert len(kutuphane.mevcut_kitaplar()) == 2

kutuphane.iade_et("978-1")
assert k1.mevcut == True

try:
    kutuphane.odunc_al("978-1")
    kutuphane.odunc_al("978-1")  # zaten alınmış
    assert False
except Exception:
    pass

print("✅ Soru 11 doğru! Tebrikler!")