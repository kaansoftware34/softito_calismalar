"""---
### Soru 6 — Kapsülleme ve @property
Bir `Sicaklik` class'ı yaz:
- `_derece` ile saklansın
- `@property` ile `derece` okunabilsin
- `@derece.setter` ile atanabilsin ama `-273`'ün altına düşürülemesin (mutlak sıfır), düşürülmeye çalışılınca `ValueError` fırlatsın
- `@property` ile `fahrenheit` hesaplansın → `(derece × 9/5) + 32`
"""

# Cevabını buraya yaz
class Sicaklik:
    def __init__(self, derece):
        self._derece = derece

    @property
    def derece(self):
        return self._derece

    @derece.setter
    def derece(self, yeni_deger):
        if yeni_deger < -273:
            raise ValueError("Sıcaklık mutlak sıfırın altına düşemez!")
        self._derece = yeni_deger

    @property
    def fahrenheit(self):
        return (self._derece * 9/5) + 32

# TEST
s = Sicaklik(100)
assert s.derece == 100
assert s.fahrenheit == 212.0
s.derece = 0
assert s.fahrenheit == 32.0
try:
    s.derece = -300
    assert False, "ValueError bekleniyor"
except ValueError:
    pass
print("✅ Soru 6 doğru!")

"""---
### Soru 7 — Dunder metotlar
Bir `Para` class'ı yaz:
- `miktar` ve `para_birimi` alsın (varsayılan: `"TL"`)
- `__str__`  → `"100 TL"` formatında
- `__add__`  → iki Para toplanabilsin (aynı para birimi kontrolü yap, farklıysa `ValueError`)
- `__eq__`   → miktar ve para birimi eşitse `True`
- `__lt__`   → miktar karşılaştırması
"""

# Cevabını buraya yaz
class Para:
    def __init__(self, miktar, para_birimi="TL"):
        self.miktar = miktar
        self.para_birimi = para_birimi

    def __str__(self):
        return f"{self.miktar} {self.para_birimi}"

    def __add__(self, diger):
        if self.para_birimi != diger.para_birimi:
            raise ValueError("Farklı para birimleri toplanamaz!")
        return Para(self.miktar + diger.miktar, self.para_birimi)

    def __eq__(self, diger):
        return self.miktar == diger.miktar and self.para_birimi == diger.para_birimi

    def __lt__(self, diger):
        if self.para_birimi != diger.para_birimi:
             raise ValueError("Farklı birimler karşılaştırılamaz!")
        return self.miktar < diger.miktar

# TEST
p1 = Para(100, "TL")
p2 = Para(50, "TL")
p3 = Para(100, "TL")
p4 = Para(50, "USD")
assert str(p1) == "100 TL"
assert (p1 + p2).miktar == 150
assert p1 == p3
assert p2 < p1
try:
    p1 + p4
    assert False, "ValueError bekleniyor"
except ValueError:
    pass
print("✅ Soru 7 doğru!")

"""---
### Soru 8 — @classmethod ve @staticmethod
Bir `Dikdortgen` class'ı yaz:
- `en` ve `boy` alsın
- `@classmethod kare_olustur(cls, kenar)` → kare döndürsün (en == boy == kenar)
- `@staticmethod gecerli_mi(en, boy)` → ikisi de 0'dan büyükse `True` döndürsün
"""

# Cevabını buraya yaz
class Dikdortgen:
    def __init__(self, en, boy):
        self.en = en
        self.boy = boy

    @classmethod
    def kare_olustur(cls, kenar):
        return cls(kenar, kenar)

    @staticmethod
    def gecerli_mi(en, boy):
        return en > 0 and boy > 0

# TEST
d = Dikdortgen(5, 3)
kare = Dikdortgen.kare_olustur(4)
