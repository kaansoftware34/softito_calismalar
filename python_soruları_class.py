"""---
## 🔴 ZOR

### Soru 9 — Iterator class
Bir `AralıkIterator` class'ı yaz:
- `baslangic`, `bitis`, `adim` alsın
- `for` döngüsünde kullanılabilsin
- Python'un `range()` gibi çalışsın ama class olarak
- `__len__` da ekle — kaç eleman üreteceğini döndürsün
"""

# Cevabını buraya yaz
class AralikIterator:
    def __init__(self, baslangic, bitis, adim):
        self.baslangic = baslangic
        self.bitis = bitis
        self.adim = adim

    def __iter__(self):
        self.mevcut = self.baslangic
        return self

    def __next__(self):
        if (self.adim > 0 and self.mevcut >= self.bitis) or \
           (self.adim < 0 and self.mevcut <= self.bitis):
            raise StopIteration
        deger = self.mevcut
        self.mevcut += self.adim
        return deger

    def __len__(self):
        return max(0, (self.bitis - self.baslangic + self.adim - (1 if self.adim > 0 else -1)) // self.adim)

# TEST
a = AralikIterator(0, 10, 2)
assert list(a) == [0, 2, 4, 6, 8]
assert len(a) == 5
b = AralikIterator(1, 6, 1)
assert list(b) == [1, 2, 3, 4, 5]
# iki kez kullanılabilmeli
assert list(a) == [0, 2, 4, 6, 8]
