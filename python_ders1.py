class Dikdortgen:
    def __init__(self, en, boy):
        self.en  = en
        self.boy = boy

    def alan(self):
        return self.en * self.boy

    def cevre(self):
        return 2 * (self.en + self.boy)

    def bilgi(self):
        print(f"Alan: {self.alan()}, Çevre: {self.cevre()}")

d = Dikdortgen(5, 3)
d.bilgi()
print(d.alan())    # 15
print(d.cevre())   # 16

