class BankaHesabi:
    def __init__(self, sahip, bakiye=0):
        self.sahip   = sahip
        self._bakiye = bakiye   # dahili

    @property
    def bakiye(self):
        return self._bakiye

    @bakiye.setter
    def bakiye(self, miktar):
        if miktar < 0:
            raise ValueError("Bakiye negatif olamaz!")
        self._bakiye = miktar

    def para_yatir(self, miktar):
        self._bakiye += miktar
        print(f"+{miktar} TL → Bakiye: {self._bakiye} TL")

    def para_cek(self, miktar):
        if miktar > self._bakiye:
            print("Yetersiz bakiye!")
        else:
            self._bakiye -= miktar
            print(f"-{miktar} TL → Bakiye: {self._bakiye} TL")

h = BankaHesabi("Ali", 1000)
h.para_yatir(500)
h.para_cek(200)
h.para_cek(2000)

class Vektor:
    def __init__(self, x, y):
        self.x = x
        self.y = y

    def __str__(self):        # print() için
        return f"({self.x}, {self.y})"

    def __repr__(self):       # geliştirici çıktısı
        return f"Vektor({self.x}, {self.y})"

    def __add__(self, other):  # + operatörü
        return Vektor(self.x + other.x, self.y + other.y)

    def __len__(self):         # len() için (boyut sayısı)
        return 2

    def uzunluk(self):
        return (self.x**2 + self.y**2) ** 0.5

v1 = Vektor(3, 4)
v2 = Vektor(1, 2)

print(v1)           # __str__
print(v1 + v2)      # __add__
print(len(v1))      # __len__
print(v1.uzunluk()) # 5.0

